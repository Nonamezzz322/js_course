function splitArray(arr, arrLength) {
  let generalArray = [];  
  let i = [];
  let j = [];
  for (i = 0, j = arr.length; i < j; i += arrLength) {
    generalArray.push(arr.slice(i, i + arrLength));
  }
  return generalArray;
}
