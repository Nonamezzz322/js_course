function revers(...arg) {
    let arr1 = [];
    for (let elem of arg) {
        let arr2 = elem.split('');
        let rev = arr2.reverse();
        arr1.push(rev.join(''));
    }
    return arr1.reverse();
}
console.log(revers('123', '456'));
